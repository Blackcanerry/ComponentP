    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {
            double input = Double.parseDouble(inputField.getText());

            if (jRadioButton1.isSelected()) {
                // Celsius to Fahrenheit
                mybean.setCelsius(input);
                double result = mybean.convertCelsiusToFahrenheit();
                JOptionPane.showMessageDialog(this,
                    String.format("%.2f 째C = %.2f 째F", input, result),
                    "Conversion Result", JOptionPane.INFORMATION_MESSAGE);
            } else if (jRadioButton2.isSelected()) {
                // Fahrenheit to Celsius
                mybean.setFahrenheit(input);
                double result = mybean.convertFahrenheitToCelsius();
                JOptionPane.showMessageDialog(this,
                    String.format("%.2f 째F = %.2f 째C", input, result),
                    "Conversion Result", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                    "Please select a conversion direction.",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Please enter a valid numeric value.",
                "Input Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

